export function feedbackRulesBlock(rules) {
  if (!rules || !rules.length) return "";
  return `\n\nПРАВИЛА КОРИСТУВАЧА (дотримуйся ЗАВЖДИ, він їх задав раніше):\n` +
    rules.map((r, i) => `${i + 1}. ${r}`).join("\n");
}

